    "orchestrator_queue_dequeue_duration_seconds",
    "Time taken to dequeue a task",
    labelnames=["agent_type"],
    buckets=(0.001, 0.005, 0.01, 0.025, 0.05, 0.1, 0.25, 0.5, 1.0),

# ---------------------------------------------------------------------------
# Helper Functions
# ---------------------------------------------------------------------------


def set_safe_mode(enabled: bool) -> None:
    """Update the safe_mode gauge to reflect current status.

    Args:
        enabled: Whether safe mode is currently enabled.
    """
    SAFE_MODE_ENABLED.set(1 if enabled else 0)


def record_task_created(agent_type: str, priority: str) -> None:
    """Record a new task creation.

    Args:
        agent_type: The agent type assigned to the task.
        priority: The task priority level.
    """
    TASKS_CREATED_TOTAL.labels(agent_type=agent_type, priority=priority).inc()


def record_root_task_created(entrypoint: str, agent_type: str) -> None:
    ROOT_TASKS_CREATED_TOTAL.labels(entrypoint=entrypoint, agent_type=agent_type).inc()


def record_planner_subtask_created(assigned_agent: str, requires_approval: bool) -> None:
    PLANNER_SUBTASKS_CREATED_TOTAL.labels(
        assigned_agent=assigned_agent,
        requires_approval=str(requires_approval).lower(),
    ).inc()


def record_planner_invalid_output() -> None:
    PLANNER_INVALID_OUTPUT_TOTAL.inc()


def record_capability_invocation(
    capability: str,
    entrypoint: str,
    status: str,
    duration_seconds: float,
) -> None:
    CAPABILITY_INVOCATIONS_TOTAL.labels(
        capability=capability,
        entrypoint=entrypoint,
        status=status,
    ).inc()
    CAPABILITY_DURATION_SECONDS.labels(
        capability=capability,
        entrypoint=entrypoint,
    ).observe(max(duration_seconds, 0.0))
    if status != "success":
        CAPABILITY_ERRORS_TOTAL.labels(
            capability=capability,
            entrypoint=entrypoint,
        ).inc()


def record_artifact_created(artifact_type: str, entrypoint: str) -> None:
    ARTIFACTS_CREATED_TOTAL.labels(
        artifact_type=artifact_type,
        entrypoint=entrypoint,
    ).inc()


def record_state_transition(
    from_state: str, to_state: str, agent_type: str = "unknown"
) -> None:
    """Record a task state transition.

    Args:
        from_state: The source state.
        to_state: The target state.
        agent_type: The agent type of the task.
    """
    TASK_STATE_TRANSITIONS_TOTAL.labels(
        from_state=from_state, to_state=to_state, agent_type=agent_type
    ).inc()


def record_task_terminal(state: str, agent_type: str = "unknown") -> None:
    """Record a task reaching a terminal state.

    Args:
        state: The terminal state (completed, failed, cancelled).
        agent_type: The agent type of the task.
    """
    TASKS_COMPLETED_TOTAL.labels(state=state, agent_type=agent_type).inc()


def update_queue_depth(agent_type: str, depth: int) -> None:
    """Update the queue depth gauge for an agent type.

    Args:
        agent_type: The agent type queue.
        depth: Current queue depth.
    """
    QUEUE_DEPTH.labels(agent_type=agent_type).set(depth)


def update_queue_depth_total(total: int) -> None:
    """Update the total queue depth gauge.

    Args:
        total: Total queue depth across all agent types.
    """
    QUEUE_DEPTH_TOTAL.set(total)


def update_worker_status(active: int, idle: int, dead: int) -> None:
    """Update worker status gauges.

    Args:
        active: Number of workers currently executing tasks.
        idle: Number of idle workers.
        dead: Number of dead workers.
    """
    WORKERS_ACTIVE.set(active)
    WORKERS_IDLE.set(idle)
    WORKERS_DEAD.set(dead)


def update_inference_slots(
    model: str, available: int, total: int, in_use: int
) -> None:
    """Update inference slot gauges for a model.

    Args:
        model: The model identifier (e.g., "code", "planner", "utility").
        available: Number of available slots.
        total: Total number of slots.
        in_use: Number of slots currently in use.
    """
    INFERENCE_SLOTS_AVAILABLE.labels(model=model).set(available)
    INFERENCE_SLOTS_TOTAL.labels(model=model).set(total)
    INFERENCE_SLOTS_IN_USE.labels(model=model).set(in_use)


def update_resource_tasks_active(agent_type: str, count: int) -> None:
    """Update the active task count for an agent type.

    Args:
        agent_type: The agent type.
        count: Number of active tasks.
    """
    RESOURCE_TASKS_ACTIVE.labels(agent_type=agent_type).set(count)


def record_error(error_type: str, component: str) -> None:
    """Record an error occurrence.

    Args:
        error_type: Classification of the error (e.g., "timeout", "connection", "validation").
        component: The module/component where the error occurred.
    """
    ERRORS_TOTAL.labels(error_type=error_type, component=component).inc()
